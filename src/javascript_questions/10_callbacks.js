/**
 * What are Callback in Javascript ? Why they used For ?
 *  
 * Callbacks are functions that are passed as arguments to other functions and are
 * executed after some operation has been completed. They are commonly used in asynchronous programming,
 * event-driven programming, and functional programming.
 * 
 * 1. Usually Callback are used for Asynchronous operations 
 * 2. Event handling (In event driven programming)
 * 3. Modularity:  Callbacks help in writing modular and reusable code.
 *  By passing functions as arguments, you can change the behavior of a function without modifying its code.

 * 4. Separation of Concerns
 * 5. Flexibility:  Callbacks provide flexibility in handling different scenarios.
 *  For instance, a single function can handle success and error scenarios by passing different callbacks.
 */


